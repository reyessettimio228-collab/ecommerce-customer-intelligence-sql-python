            WHEN clv_score = 3 THEN 'Medium Value'
            WHEN clv_score = 2 THEN 'Low Value'
            ELSE 'Very Low Value'
        END AS customer_value_tier
    FROM tiered_customers
) c
    ON r.customer_id = c.customer_id
ORDER BY c.historical_clv DESC;


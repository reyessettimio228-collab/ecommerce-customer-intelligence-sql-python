            WHEN clv_score = 3 THEN 'Medium Value'
            WHEN clv_score = 2 THEN 'Low Value'
            ELSE 'Very Low Value'
        END AS customer_value_tier
    FROM tiered_customers
)

SELECT
    customer_value_tier,
    COUNT(*) AS number_of_customers,
    ROUND(SUM(historical_clv), 2) AS total_historical_clv,
    ROUND(AVG(historical_clv), 2) AS avg_historical_clv,
    ROUND(AVG(estimated_annualized_clv), 2) AS avg_estimated_annualized_clv,
    ROUND(AVG(total_orders), 2) AS avg_orders_per_customer,
    ROUND(AVG(average_order_value), 2) AS avg_order_value
FROM customer_tiers
GROUP BY customer_value_tier
ORDER BY
    CASE
        WHEN customer_value_tier = 'Very High Value' THEN 1
        WHEN customer_value_tier = 'High Value' THEN 2
        WHEN customer_value_tier = 'Medium Value' THEN 3
        WHEN customer_value_tier = 'Low Value' THEN 4
        ELSE 5
    END;

-- =============================================================================
-- 6. CLV and RFM combined view
-- =============================================================================
-- This combines customer lifetime value with the RFM segments created in the
-- previous script.

SELECT
    r.customer_id,
    r.customer_segment,
    r.recency_days,
    r.frequency,
    r.monetary_value AS rfm_monetary_value,
    c.total_orders,
    c.customer_lifespan_days,
    c.average_order_value,
    c.historical_clv,
    c.estimated_annualized_clv,
    c.customer_value_tier
FROM vw_rfm_segments r
JOIN (
    WITH customer_orders AS (
        SELECT
            customer_id,
            invoice_no,
            MIN(invoice_date) AS order_date,
            ROUND(SUM(line_total), 2) AS order_revenue

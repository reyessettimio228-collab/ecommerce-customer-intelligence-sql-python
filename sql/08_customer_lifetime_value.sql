),

customer_metrics AS (
    SELECT
        customer_id,
        COUNT(DISTINCT invoice_no) AS total_orders,
        MIN(order_date) AS first_order_date,
        MAX(order_date) AS last_order_date,
        DATE_PART('day', MAX(order_date) - MIN(order_date)) AS customer_lifespan_days,
        ROUND(SUM(order_revenue), 2) AS historical_clv,
        ROUND(AVG(order_revenue), 2) AS average_order_value
    FROM customer_orders
    GROUP BY customer_id
),

customer_clv AS (
    SELECT
        customer_id,
        total_orders,
        first_order_date,
        last_order_date,
        customer_lifespan_days,
        average_order_value,
        historical_clv,
        CASE
            WHEN customer_lifespan_days > 0
                THEN ROUND(historical_clv / customer_lifespan_days * 365, 2)
            ELSE historical_clv
        END AS estimated_annualized_clv
    FROM customer_metrics
),

tiered_customers AS (
    SELECT
        *,
        NTILE(5) OVER (ORDER BY historical_clv ASC) AS clv_score
    FROM customer_clv
)

SELECT
    customer_id,
    total_orders,
    customer_lifespan_days,
    average_order_value,
    historical_clv,
    estimated_annualized_clv,
    clv_score,
    CASE
        WHEN clv_score = 5 THEN 'Very High Value'
        WHEN clv_score = 4 THEN 'High Value'
        WHEN clv_score = 3 THEN 'Medium Value'
        WHEN clv_score = 2 THEN 'Low Value'
        ELSE 'Very Low Value'
    END AS customer_value_tier
FROM tiered_customers
ORDER BY historical_clv DESC;

